<?php

namespace Tests\Feature;

use App\Models\User;
use Tests\TestCase;

class RepairShopTest extends TestCase
{
    public function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->authenticate();
    }

    public function authenticate(string $guard = null)
    {
        $user = User::factory()->create();
        $this->actingAs($user, $guard);
    }

    public function test_load_repair_shops_list_with_auth()
    {
        $response = $this->get('/repair-shop');
        $response->assertStatus(200);
        $response->assertSuccessful();
        $response->assertViewIs('pages.shops.index');

    }

